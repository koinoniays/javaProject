package stream2;

public class Student {
	private String name;
	private int mScore;
	private int eScore;
	
	
	
	public Student (String name, int mScore, int eScore) {
		this.name = name;
		this.mScore = mScore;
		this.eScore = eScore;
	}
	
	

	public String getName() {
		return name;
	}
	public int getmScore() {
		return mScore;
	}
	public int geteScore() {
		return eScore;
	}
		

}
